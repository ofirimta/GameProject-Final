class Point {//default values
	int x = 1, y = 1;
	int diff_x = 1, diff_y = 0;
	char ch = ' ';

public:

	Point() {}
	//////////////////////////////////////////////////////////
	Point(int x1, int y1, int diffx, int diffy, char c)
	{
		x = x1;
		y = y1;
		diff_x = diffx;
		diff_y = diffy;
		ch = c;
	}
	/////////////////////////////////////////////////////////
	void draw() const {
		draw(ch);
	}
	//////////////////////////////////////////////////////////
	void draw(char c) const {
		gotoxy(x, y);
		cout << c;
	}
	////////////////////////////////////////////////////////
	void move();

	////////////////////////////////////////////////////////
	void setDirection(Direction dir);
	////////////////////////////////////////////////////////
	void setX(int new_x) { x = new_x; }
	////////////////////////////////////////////////////////
	void setY(int new_y) { y = new_y; }
	////////////////////////////////////////////////////////
	void setChar(char c);
	////////////////////////////////////////////////////////
	int getX() const {
		return x;
	}
	////////////////////////////////////
	int getY() const {
		return y;
	}
	///////////////////////////////////
	char getChar() const
	{
		return ch;
	}
	///////////////////////////////////
	int getdiffX() const {
		return diff_x;
	}
	///////////////////////////////////
	int getdiffY() const {
		return diff_y;
	}
	///////////////////////////////////////////////////////////
	Point nextPos() const;
	//////////////////////////////////////////////////////////UPDATES FROM HERE

	Point prevPos() const;
};